void dfs_12035(int x, int y) {
	total_area++;
	book_12035[x][y] = 1;
	if (x - 1 > 0 && book_12035[x - 1][y] == 0 && map_12035[x-1][y]!=0) {
		dfs_12035(x - 1, y);
	}
	if (x + 1 <=n_12035 && book_12035[x + 1][y] == 0 && map_12035[x+ 1][y] != 0) {
		dfs_12035(x+ 1, y);
	}
	if (y - 1 > 0 && book_12035[x][y-1] == 0 && map_12035[x][y-1] != 0) {
		dfs_12035(x, y-1);
	}
	if (y + 1 <=m_12035 && book_12035[x][y+1] == 0 && map_12035[x][y+1] != 0) {
		dfs_12035(x, y+1);
	}
}